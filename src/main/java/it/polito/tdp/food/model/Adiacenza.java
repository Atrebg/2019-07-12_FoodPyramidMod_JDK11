package it.polito.tdp.food.model;

public class Adiacenza {

	int f1;
	int f2;
	
	long media;

	public Adiacenza(int f1, int f2, long media) {
		super();
		this.f1 = f1;
		this.f2 = f2;
		this.media = media;
	}

	public int getF1() {
		return f1;
	}

	public void setF1(int f1) {
		this.f1 = f1;
	}

	public int getF2() {
		return f2;
	}

	public void setF2(int f2) {
		this.f2 = f2;
	}

	public long getMedia() {
		return media;
	}

	public void setMedia(long media) {
		this.media = media;
	}
	
	
	
}
